#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int	ret;

	ret = 0;
	if (s1 && s2)
	{
		ret = (ft_strcmp(s1, s2) == 0) ? 1 : 0;
	}
	return (ret);
}
